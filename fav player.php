<h3>Indian Premier League</h3>
<p>Dhoni was contracted by the Chennai Super Kings for US$1.5 million. This made him the most expensive player in the IPL for the first season auctions.[136] Under his captaincy, Chennai Super Kings won the 2010 and 2011 and 2018 Indian Premier League titles and the 2010 and 2014 Champions League T20 titles.[137]

After the suspension of CSK for two years, he was bought by Rising Pune Supergiant for US$1.9  million in 2016, and was named captain. However, his team finished in 7th place. In 2017, his team reached the final, where they lost to Mumbai Indians.

In the 2018 IPL season, Chennai Super Kings returned to IPL, and Dhoni was again selected to lead the franchise. Dhoni scored 455 runs in the tournament and led his side to their third IPL title.[138]

In the 2019 IPL, Dhoni again captained CSK, which emerged to be one of the strongest teams in the season. Dhoni was the leading run-scorer for the team, which included 3 fifties - 75* and 58 against RR in winning causes and 84* against RCB in a losing cause, in which CSK lost by a run.<p>

