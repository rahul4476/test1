<h3>international records</h3>
<p>Test cricket
Under Dhoni's captaincy, India topped the Test cricket rankings for the first time, in 2009.[148]
Dhoni is the most successful Indian Test captain with 27 Test wins, eclipsing Sourav Ganguly's record of 21.[125]
Dhoni has the most overseas Test defeats by an Indian captain, with 15.[149][150][151]
Dhoni is the first Indian wicket-keeper to complete 4,000 Test runs.[152]
Dhoni's 224 against Australia in Chennai is the third highest score by an Indian captain.[153] En route to 224, Dhoni registered the highest Test score by an Indian wicket-keeper when he was on 193, beating Budhi Kunderan's 192. It was also the highest score by a wicket-keeper–captain beating Englishman Alec Stewart's 164.
Dhoni's maiden century against Pakistan in Faisalabad (148) is the fastest century scored by an Indian wicket-keeper, and fourth overall.[154]
After hitting a six in the third Test against England in Southampton, Dhoni completed 50 sixes as a captain, an Indian record.[155]
Dhoni, with 294 dismissals in his career, ranks first in the all-time dismissals list by Indian wicket-keepers.[156]
Dhoni shares the record for most dismissals in an innings (6, with Syed Kirmani) and in a match (9) by an Indian wicket-keeper.[157]
ODI cricket
See also: List of highest individual scores in ODIs and List of players who have scored 10,000 or more runs in One Day International cricket
Dhoni is third captain (and the first non-Australian) overall to win 100 games.[135]
Fourth Indian to reach 10,000 ODI runs after Sachin Tendulkar, Sourav Ganguly & Rahul Dravid and also the second wicket-keeper to reach the milestone.[158]
First player to pass 10,000 runs in ODI cricket with having a career average of over 50.[159]
Dhoni has the fifth highest batting average (51.09), among cricketers with more than 5,000 runs and the second highest batting average among players with an aggregate of over 10,000 runs.[160]
Most career runs in ODI history when batting at number 6 position (4031)[161]
Only player to score more than one hundred in ODI cricket when batting at number 7 position or lower (Dhoni has 2 centuries at number 7).[162]
Most not outs (82) in ODIs.
First Indian and fifth overall to hit 200 sixes in ODIs. [163]
Dhoni's 183* against Sri Lanka in 2005 is the highest score by a wicket-keeper.[164]
Dhoni's 113 against Pakistan in Chennai in 2012 is the highest by a captain batting at number 7. [165]
Dhoni and Bhuvneshwar Kumar were involved in a partnership of 100 not out against Sri Lanka, which is India's highest eighth wicket partnership in ODIs.[166]
Most unbeaten innings and highest average (among batsmen with more that 20 such innings) in successful ODI run-chases.[167]
Holds the record for playing the most matches in ODI history as captain who has also served as a wicket-keeper(200)[168]
Dhoni holds the records of the most dismissals in an innings (6) and career (432) by an Indian wicket-keeper.[169]
Dhoni has the most stumpings (120) by any wicket-keeper in an ODI career and is so far the only keeper to pass 100 stumpings[170][171]
First Indian wicket-keeper to take 300 ODI catches and fourth wicket-keeper in the world to achieve the feat.[172]
